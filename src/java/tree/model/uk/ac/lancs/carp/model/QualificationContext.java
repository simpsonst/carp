package uk.ac.lancs.carp.model;

/**
 * Provides a context for resolving local external names to fully
 * qualified ones.
 * 
 * @author simpsons
 */
public interface QualificationContext extends Qualifier {
    /**
     * Copy associations from an old object to its replacement. A model
     * element should use this to transfer caller-defined associations
     * of the old instance to the replacement.
     * 
     * @param to the object that associations should be assigned to
     * 
     * @param from the object whose associations should be copied from
     */
    void copyAssociations(Object to, Object from);
}
